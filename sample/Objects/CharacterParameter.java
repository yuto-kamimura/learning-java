package sample.Objects;

public class CharacterParameter {
    public int hp;
    public int power;
    public int deffence;
    public int speed;
    public int mp;

    public CharacterParameter(int hp, int power, int deffence, int speed, int mp) {
        this.hp = hp;
        this.power = power;
        this.deffence = deffence;
        this.speed = speed;
        this.mp = mp;
    }
}
